package OOPS;

public class T023_ForLoop {
    public static void main(String[] args) {
        System.out.println("FOR LOOP --->");
        for(int i=1;i<=10;i=i+2){
            System.out.println(i);
        }
        /*
        2n = Even number = 0,2,4,6,8.....
        2n+1 = Odd number = 1,3,5,7,9.....
         */
    }
}
