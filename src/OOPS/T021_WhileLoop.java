package OOPS;

public class T021_WhileLoop {
    public static void main(String[] args) {
        System.out.println("TYPES OF LOOP ===>");
        System.out.println();
        System.out.println("WHILE LOOP ---> ");
        int i = 1;
        while (i<=3){
            System.out.println(i);
            i++;
        }
        System.out.println("FINISH RUNNING WHILE LOOP");
        System.out.println();
//        while (true){
//            System.out.println("I AM A INFINITE WHILE LOOP");
//        }
        //To print natural numbers between 100 and 200
        int n=100;
        while (n<=200){
            System.out.println(n);
            n++;
        }
    }
}
